package org.informatics.data.enums;

import java.io.Serializable;

/**
 * <p>Represents the biological gender designation of personnel within the enterprise ecosystem.</p>
 * <p>This enumeration enforces structural data type boundaries.</p>
 */
public enum Gender implements Serializable {
    /**
     * <p>Represents male personnel attributes.</p>
     */
    MALE,

    /**
     * <p>Represents female personnel attributes.</p>
     */
    FEMALE;

}
